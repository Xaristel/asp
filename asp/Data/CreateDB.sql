CREATE DATABASE Univercity;
GO

USE Univercity;

CREATE TABLE Course 
(
  CourseId INT NOT NULL PRIMARY KEY IDENTITY(1,1), 
  CourseName CHAR(30),
  CourseDescription CHAR(100)
);
GO

CREATE TABLE "Group" 
(
  GroupId INT NOT NULL PRIMARY KEY IDENTITY(1,1),
  CourseId INT NOT NULL,
  GroupName CHAR(30)
);
GO

ALTER TABLE "Group"
WITH CHECK ADD CONSTRAINT FK_GROUPS_COURCES FOREIGN KEY(CourseId)
REFERENCES Course (CourseId)
ON UPDATE CASCADE
ON DELETE CASCADE
GO

CREATE TABLE Student
(
  StudentId INT NOT NULL PRIMARY KEY IDENTITY(1,1),
  GroupId INT NOT NULL,
  FirstName CHAR(30),
  LastName CHAR(30)
);
GO

ALTER TABLE Student
WITH CHECK ADD CONSTRAINT FK_STUDENTS_GROUPS FOREIGN KEY(GroupId)
REFERENCES "Group" (GroupId)
ON UPDATE CASCADE
ON DELETE CASCADE
GO